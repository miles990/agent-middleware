DEFAULT_DATA_DIR = Path.home() / "Workspace" / "mini-agent" / "kuro-portfolio" / "ai-trend" / "data"
DATA_DIR = Path(os.environ.get("AI_TREND_DATA_DIR", str(DEFAULT_DATA_DIR)))


@mcp.tool
async def resolve_date_range(expression: str) -> str:
    """Canonicalize natural-language date expressions to {start, end} ISO dates.

    Pre-resolve here so multiple LLM clients agree on what 'this week' means.
    Supported: today, yesterday, this-week, last-week, last-N-days, YYYY-MM-DD,
    YYYY-MM-DD..YYYY-MM-DD.
    """
    today = date.today()
    e = expression.strip().lower()

    if e in ("today",):
        s = today
    elif e in ("yesterday",):
        s = today - timedelta(days=1)
        return json.dumps({"start": s.isoformat(), "end": s.isoformat()})
    elif e in ("this-week", "this_week", "本周", "本週"):
        s = today - timedelta(days=today.weekday())
        return json.dumps({"start": s.isoformat(), "end": today.isoformat()})
    elif e in ("last-week", "last_week", "上周", "上週"):
        end = today - timedelta(days=today.weekday() + 1)
        s = end - timedelta(days=6)
        return json.dumps({"start": s.isoformat(), "end": end.isoformat()})
    elif m := re.fullmatch(r"last-(\d+)-days?", e):
        n = int(m.group(1))
        s = today - timedelta(days=n - 1)
        return json.dumps({"start": s.isoformat(), "end": today.isoformat()})
    elif m := re.fullmatch(r"(\d{4}-\d{2}-\d{2})\.\.(\d{4}-\d{2}-\d{2})", e):
        return json.dumps({"start": m.group(1), "end": m.group(2)})
    elif re.fullmatch(r"\d{4}-\d{2}-\d{2}", e):
        return json.dumps({"start": e, "end": e})
    else:
        return json.dumps({"error": f"unrecognized: {expression}"})

    return json.dumps({"start": s.isoformat(), "end": today.isoformat()})


@mcp.tool
async def query_archive(
    date_range: Optional[str] = None,
    topic: Optional[str] = None,
    limit: int = 20,
) -> str:
    """Query Kuro's ai-trend opinion archive.

    Args:
        date_range: ISO date or date..date or natural expr (call resolve_date_range first)
        topic: filter by topic tag (e.g. 'cs.MA', 'paper-opinion')
        limit: max entries

    Returns: JSON list of {date, title, opinion, source_url, kg_node_id?}.
    STUB — backed by real archive next cycle (target: kuro.page raw markdown).
    """
    # Resolve window → pick which landing-{N}d.json to read.
    start, end = _parse_range(date_range)
    if start and end:
        span_days = (end - start).days + 1
    else:
        span_days = 7

    if span_days <= 1:
        backend = DATA_DIR / "landing-1d.json"
        period_used = "1d"
    elif span_days <= 7:
        backend = DATA_DIR / "landing-7d.json"
        period_used = "7d"
    else:
        backend = DATA_DIR / "landing-30d.json"
        period_used = "30d"

    if not backend.exists():
        return json.dumps(
            {"error": f"backend not found: {backend}", "data_dir": str(DATA_DIR)},
            ensure_ascii=False,
        )

    payload = json.loads(backend.read_text(encoding="utf-8"))
    topics = payload.get("top_topics", [])
    if topic:
        needle = topic.lower()
        topics = [t for t in topics if needle in t.get("topic", "").lower()]

    entries: list[dict] = []
    for t in topics:
        for s in t.get("sample_titles", []):
            entries.append(
                {
                    "topic": t.get("topic"),
                    "post_count": t.get("post_count"),
                    "title": s.get("title"),
                    "url": s.get("url"),
                    "source": s.get("source"),
                    "points": s.get("points"),
                    "zh_claim": s.get("zh_claim"),
                    "zh_so_what": s.get("zh_so_what"),
                    "published_at": s.get("published_at"),
                }
            )
            if len(entries) >= limit:
                break
        if len(entries) >= limit:
            break

    return json.dumps(
        {
            "meta": {
                "period_used": period_used,
                "backend": str(backend.name),
                "generated_at": payload.get("meta", {}).get("generated_at"),
                "window_start": payload.get("meta", {}).get("start_date"),
                "window_end": payload.get("meta", {}).get("end_date"),
                "topic_filter": topic,
                "returned": len(entries),
            },
            "entries": entries,
        },
        ensure_ascii=False,
    )


def _parse_range(expr: Optional[str]) -> tuple[Optional[date], Optional[date]]:
    """Best-effort parse of date_range arg. Accepts ISO date, ISO..ISO, or NL expr."""
    if not expr:
        return None, None
    e = expr.strip().lower()
    today = date.today()
    try:
        if e == "today":
            return today, today
        if e == "yesterday":
            d = today - timedelta(days=1)
            return d, d
        if e in ("this-week", "this_week"):
            return today - timedelta(days=today.weekday()), today
        if m := re.fullmatch(r"last-(\d+)-days?", e):
            n = int(m.group(1))
            return today - timedelta(days=n - 1), today
        if m := re.fullmatch(r"(\d{4}-\d{2}-\d{2})\.\.(\d{4}-\d{2}-\d{2})", e):
            return (
                datetime.strptime(m.group(1), "%Y-%m-%d").date(),
                datetime.strptime(m.group(2), "%Y-%m-%d").date(),
            )
        if re.fullmatch(r"\d{4}-\d{2}-\d{2}", e):
            d = datetime.strptime(e, "%Y-%m-%d").date()
            return d, d
    except Exception:
        return None, None
    return None, None


@mcp.tool
async def list_topics(period: str = "7d") -> str:
    """List unique topics in archive with post counts.

    Use to discover valid filter values for `query_archive(topic=...)` without
    having to query first and parse the response. Reads top_topics from
    landing-{period}.json directly.

    Args:
        period: "1d" / "7d" / "30d" (defaults to 7d)

    Returns: JSON {period, generated_at, window, topics: [{topic, post_count}]}
    """
    backend_map = {"1d": "landing-1d.json", "7d": "landing-7d.json", "30d": "landing-30d.json"}
    if period not in backend_map:
        return json.dumps(
            {"error": f"period must be one of {list(backend_map)}, got '{period}'"}
        )

    backend = DATA_DIR / backend_map[period]
    if not backend.exists():
        return json.dumps(
            {"error": f"backend not found: {backend}", "data_dir": str(DATA_DIR)},
            ensure_ascii=False,
        )

    payload = json.loads(backend.read_text(encoding="utf-8"))
    meta = payload.get("meta", {})
    topics = [
        {"topic": t.get("topic"), "post_count": t.get("post_count")}
        for t in payload.get("top_topics", [])
        if t.get("topic")
    ]
    return json.dumps(
        {
            "period": period,
            "generated_at": meta.get("generated_at"),
            "window": {
                "start": meta.get("start_date"),
                "end": meta.get("end_date"),
            },
            "topics": topics,
        },
        ensure_ascii=False,
    )


@mcp.tool
async def read_article(url: str) -> str:
    """Fetch article content via Jina Reader (r.jina.ai).

    Solves P2 #43: ai-trend posts cite source URLs but the LLM that wrote the
    opinion didn't have full content. This tool lets readers (or downstream
    agents) reify a citation back to its source markdown.

    Free tier: 100 RPM. Returns markdown.
    """
    import urllib.request

    req = urllib.request.Request(
        f"https://r.jina.ai/{url}",
        headers={"Accept": "text/markdown", "User-Agent": "kuro-ai-trend-mcp/0.1"},
    )
    try:
        with urllib.request.urlopen(req, timeout=20) as resp:
            return resp.read().decode("utf-8", errors="replace")
    except Exception as exc:
        return json.dumps({"error": str(exc), "url": url})


if __name__ == "__main__":
    mcp.run()
